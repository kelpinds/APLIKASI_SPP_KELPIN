    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
       
        String i = ni.getText();
        String n = nis.getText();
        String nm = nama.getText();
        String l = (String) kelas.getSelectedItem();
        String a = alamat.getText();
        String o = no.getText();
        String s = (String) spp.getSelectedItem();
      
        
        String sql = "insert into siswa(nisn,nis,nama,id_kelas,alamat,no_telp,id_spp) values('"+i+"','"+n+"','"+nm+"','"+l+"','"+a+"','"+o+"','"+s+"')";
        
        koneksi md = new koneksi();
        if(md.rubahdata(sql)){
            status.setText("data berhasil ditambah");
            buattabel();
            
        }else{
            status.setText("data gagal ditambah");
        }
        kosong();
    }//GEN-LAST:event_simpanMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
         int nobaris = tabel.getSelectedRow();
        String kode = tabel.getValueAt(nobaris, 0).toString();
        String n = nis.getText();
        String nm = nama.getText();
        String l = (String) kelas.getSelectedItem();
        String a = alamat.getText();
        String o = no.getText();
        String s = (String) spp.getSelectedItem();
      
        
       String sql = "update siswa set nis='"+n+"',nama='"+nm+"',id_kelas='"+l+"',alamat='"+a+"',no_telp='"+o+"',id_spp='"+s+"' where nisn="+kode;
        
        koneksi md = new koneksi();
        if(md.rubahdata(sql)){
            status.setText("data berhasil ditambah");
            buattabel();
            
        }else{
            status.setText("data gagal ditambah");
        }
        kosong();
    }//GEN-LAST:event_editMouseClicked

