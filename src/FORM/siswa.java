private void buattabel() {
        DefaultTableModel kp = (DefaultTableModel) tabel.getModel();

        kp.getDataVector().removeAllElements();

        String sql = "select * from siswa";

        koneksi md = new koneksi();

        ResultSet rs = md.ambildata(sql);
        try {
            while (rs.next()) {
               Vector baris = new Vector();
               baris.add(rs.getString("nisn"));
               baris.add(rs.getString("nis"));
               baris.add(rs.getString("nama"));
               baris.add(rs.getInt("id_kelas"));
               baris.add(rs.getString("alamat"));
               baris.add(rs.getString("no_telp"));
               baris.add(rs.getInt("id_spp"));
               kp.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        tabel.setModel(kp);

    }
public void siswa(){
    koneksi md = new koneksi();
    String sql = "select * from kelas"; 
    ResultSet rs = md.ambildata(sql);
    try { 
        while (rs.next()){
            kelas.addItem(String.valueOf(rs.getInt("id_kelas")));
            
    } 
    } catch (SQLException ex) {
        Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex); 
    }
}
